// 中国主要城市经纬度 → 映射到 SVG 坐标系 (0-600, 0-500)
// 坐标基于墨卡托投影简化版，x: 经度映射, y: 纬度映射（翻转）
// 经度范围 73~135 → x 0~600, 纬度范围 18~54 → y 500~0

function toSvg(lng: number, lat: number): [number, number] {
  const x = ((lng - 73) / (135 - 73)) * 600;
  const y = ((54 - lat) / (54 - 18)) * 500;
  return [Math.round(x * 10) / 10, Math.round(y * 10) / 10];
}

