export const CITY_COORDS: CityCoord[] = Object.entries(RAW).map(([city, [lng, lat]]) => ({
  city,
  pos: toSvg(lng, lat),
}));

export function getCityCoord(cityName: string): [number, number] | null {
  const entry = CITY_COORDS.find(c => c.city === cityName);
  return entry ? entry.pos : null;
}

export function getCityLngLat(cityName: string): [number, number] | null {
  const coord = RAW[cityName];
  return coord ?? null;
}
